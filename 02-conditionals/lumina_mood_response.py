# Lumina Mood Response v0.2

mood = input("Hello, how are you feeling today? ").strip().lower()

if mood == "sad":
    print("Don't be sad. God loves you and has a purpose for your life.")
elif mood == "worried":
    print("Have peace in God. The Bible says He carries our worries. Rest in Him.")
elif mood == "happy":
    print("I'm glad you're happy. Keep shining, God created you with joy.")
elif mood == "afraid":
    print("Do not be afraid. God is always with you.")
else:
    print("God bless you.")